def calc_crc(message: bytes):
    crc_hi = 0xff
    crc_lo = 0xff
    pos = 0
    length = len(message)

    while pos < length:
        index = crc_lo ^ message[pos]
        crc_lo = crc_hi ^ lookup_crc_hi[index]
        crc_hi = lookup_crc_lo[index]
        pos += 1

    return [crc_lo, crc_hi]


